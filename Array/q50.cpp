/****************************************************************************************************
                                         50. Pow(x, n)    
-----------------------------------------------------------------------------------------------------
Implement pow(x, n).
****************************************************************************************************/

class Solution {
public:
    double myPow(double x, int n) {
        
        if (n==0) return 1;
        if (n==1) return x;
        if (n==-1) return 1/x;
        double tmp = myPow(x,n/2);
        if (n%2!=0) return n>0?tmp*tmp*x:tmp*tmp/x;
        return tmp*tmp;
    }
};

