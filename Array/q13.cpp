/****************************************************************************************************
                                       13. Roman to Integer      
-----------------------------------------------------------------------------------------------------
Given a roman numeral, convert it to an integer.

Input is guaranteed to be within the range from 1 to 3999.
****************************************************************************************************/

class Solution {
public:
    int romanToInt(string s) {
        vector<int> rtoi; rtoi.resize(26);
        rtoi['M'-'A']=1000;
        rtoi['D'-'A']=500;
        rtoi['C'-'A']=100;
        rtoi['L'-'A']=50;
        rtoi['X'-'A']=10;
        rtoi['V'-'A']=5;
        rtoi['I'-'A']=1;
        
        int len=s.size();
        int rst=0;
        for (int i=0;i<len;i++)
        {
            rst+=rtoi[s[i]-'A'];
            if(i>0 && rtoi[s[i]-'A']>rtoi[s[i-1]-'A'])
            {
                rst-=2*rtoi[s[i-1]-'A'];
            }
        }
        return rst;
    }
};

